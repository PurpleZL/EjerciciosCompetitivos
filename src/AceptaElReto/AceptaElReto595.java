package AceptaElReto;

public class AceptaElReto595 {

    static java.util.Scanner sc = new java.util.Scanner(System.in);

    public static void main(String[] args) {
        int casos, numeros;
        casos=sc.nextInt();
        for (int i = 0; i < casos; i++) {
            numeros=sc.nextInt();
            System.out.println(numeros/100);
        }
    }
}
